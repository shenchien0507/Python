# 複合指定運算子練習

# +=
x = 3
x += 2
print("x += 2 的結果：", x)

# -=
x = 3
x -= 2
print("x -= 2 的結果：", x)

# *=
x = 3
x *= 2
print("x *= 2 的結果：", x)

# /=
x = 3
x /= 2
print("x /= 2 的結果：", x)

# //=
x = 3
x //= 2
print("x //= 2 的結果：", x)

# %=
x = 3
x %= 2
print("x %= 2 的結果：", x)

# **=
x = 3
x **= 2
print("x **= 2 的結果：", x)

