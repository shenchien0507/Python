# 字串常值
print('python') # 顯示字串常值'Python'>>Python
print("3.8") # 顯示字串常值"3.8">>3.8
print("This's a book.") # 字串常值中有單引(')時要用雙引(" ")>>This's a book.
print('"Hi!" says Jack.') # 字串常值中有雙引(")時要用單引(' ')>>"Hi!" says Jack.

